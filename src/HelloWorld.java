/*
 * Public java keyword is an access modifier. An access modifier allows
 * us to define the scope or how other parts of your code or even some
 * else's code can access this code.
 * Class keyword is used to define a class with the name following the keyword.
 * Method is a collection of statements (one or more) that performs an operation.
 */
public class HelloWorld {
    public static void main(String[] args) {
        System.out.println("Hello World!");
        // Statement is a complete command to be executed and can include one
        // or more expressions
    }
}
